// 38. Cambia el valor de una variable declarada con var y observa el resultado.

var edad = 37;

//Mostrar el valor la primera vez.
console.log(`Primer valor de edad: ` + edad);

//Reasignar el valor de la variable.
edad = 38;

console.log(`Segundo valor de edad: ` + edad);

//Cambiar el valor de la variable.
edad = edad + 1;

console.log(`Tercer valor de edad: ` + edad);